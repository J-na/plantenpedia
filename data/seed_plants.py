# ── Ontdubbelen op wetenschappelijke naam ─────────────────────────────────────

def deduplicate(plants: list[dict]) -> list[dict]:
    seen: dict[str, dict] = {}
    for p in plants:
        key = p["scientific_name"].strip().lower()
        if key not in seen:
            seen[key] = p
        else:
            # Voeg Nederlandse namen samen
            existing_names = set(seen[key].get("dutch_names") or [])
            new_names = set(p.get("dutch_names") or [])
            seen[key]["dutch_names"] = sorted(existing_names | new_names)
    return list(seen.values())


# ── Seed-functie ─────────────────────────────────────────────────────────────

def seed(dry_run: bool = False) -> None:
    plants = deduplicate(PLANTS)
    print(f"Seeden van {len(plants)} unieke plantensoorten...")

    if dry_run:
        for p in plants:
            print(f"  [DRY RUN] {p['scientific_name']}")
        return

    client = get_client()

    success = 0
    errors = 0
    for plant in plants:
        plant["slug"] = make_slug(plant["scientific_name"])
        try:
            client.table("plants").upsert(plant, on_conflict="scientific_name").execute()
            print(f"  ✓ {plant['scientific_name']}")
            success += 1
        except Exception as exc:
            print(f"  ✗ {plant['scientific_name']}: {exc}")
            errors += 1

    print(f"\nKlaar: {success} succesvol, {errors} fouten.")


