# ── Supabase connectie ────────────────────────────────────────────────────────

def get_client():
    # Laad secrets handmatig als het script buiten Streamlit draait
    try:
        url = st.secrets["supabase"]["url"]
        key = st.secrets["supabase"].get("service_key") or st.secrets["supabase"]["key"]
    except Exception:
        # Fallback: lees uit omgevingsvariabelen
        url = os.environ.get("SUPABASE_URL", "")
        key = os.environ.get("SUPABASE_SERVICE_KEY", os.environ.get("SUPABASE_KEY", ""))
        if not url or not key:
            print("ERROR: stel SUPABASE_URL en SUPABASE_SERVICE_KEY in als omgevingsvariabelen,")
            print("       of zorg dat .streamlit/secrets.toml correct is ingevuld.")
            sys.exit(1)
    return create_client(url, key)


# ── Hulpfuncties ──────────────────────────────────────────────────────────────

def make_slug(scientific_name: str) -> str:
    slug = scientific_name.lower().strip()
    slug = re.sub(r"[^a-z0-9]+", "-", slug)
    return slug.strip("-")


