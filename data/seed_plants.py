if __name__ == "__main__":
    import argparse
    parser = argparse.ArgumentParser(description="Seed Plantenpedia database")
    parser.add_argument("--dry-run", action="store_true", help="Alleen tonen, niet schrijven")
    args = parser.parse_args()
    seed(dry_run=args.dry_run)
